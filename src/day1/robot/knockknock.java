package day1.robot;

import javax.swing.JOptionPane;

public class knockknock {
	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null, "Sally went running with scissors");
		JOptionPane.showMessageDialog(null, "She fell down and cut all her limbs off");
		JOptionPane.showMessageDialog(null, "Knock Knock");
		JOptionPane.showMessageDialog(null, "Who's there?");
		JOptionPane.showMessageDialog(null, "Not Sally");
	}

}
